    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        try {
            int i = jTable1.getSelectedRow();
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            PlayerNumber.setText(model.getValueAt(i, 0).toString());
            FName.setText(model.getValueAt(i, 1).toString());
            MName.setText(model.getValueAt(i, 2).toString());
            LName.setText(model.getValueAt(i, 3).toString());
            Position.setText(model.getValueAt(i, 4).toString());
            Weight.setText(model.getValueAt(i, 5).toString());
            Height.setText(model.getValueAt(i, 6).toString());
            Goals.setText(model.getValueAt(i, 7).toString());
            
            Date date = (Date) model.getValueAt(i, 8); //Date format
            PDOB.setDate(date);
            TeamID.setText(model.getValueAt(i, 9).toString());
           
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }


    }//GEN-LAST:event_jTable1MouseClicked

