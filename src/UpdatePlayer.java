            String sql = "Update Player set FName = ?, MName = ?, LName = ?, Position_ = ?, Weight = ?, Hieght = ?, Goals = ?,  DoB = ?, TeamID = ? where PlayerNo = ? ";

            Connection conn = DriverManager.getConnection("your DB values goes here", "scott", "tiger");
            PreparedStatement pstmt = conn.prepareStatement(sql);
            pstmt.setString(1, FName.getText());
            pstmt.setString(2, MName.getText());
            pstmt.setString(3, LName.getText());
            pstmt.setString(4, Position.getText());
            pstmt.setInt(5, Integer.parseInt(Weight.getText()));
            pstmt.setInt(6, Integer.parseInt(Height.getText()));
            pstmt.setInt(7, Integer.parseInt(Goals.getText()));
            //DATE format/methods
            SimpleDateFormat sdf = new SimpleDateFormat("d-MMM-yyyy"); //Date format
            String date = sdf.format(PDOB.getDate());
            pstmt.setString(8, date);
            //End of date
            pstmt.setInt(9, Integer.parseInt(TeamID.getText()));
            pstmt.setInt(10, Integer.parseInt(PlayerNumber.getText()));
            
            pstmt.executeUpdate(); //excute it
            JOptionPane.showMessageDialog(null, "Sucessfully Updated");
            conn.close();
            displayTable();
